package com.ms.entity;

public class Multimedia {
	private String email;
	private String music;
	private String video;
	private String fmRadio;
	private String documentReader;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMusic() {
		return music;
	}

	public void setMusic(String music) {
		this.music = music;
	}

	public String getVideo() {
		return video;
	}

	public void setVideo(String video) {
		this.video = video;
	}

	public String getFmRadio() {
		return fmRadio;
	}

	public void setFmRadio(String fmRadio) {
		this.fmRadio = fmRadio;
	}

	public String getDocumentReader() {
		return documentReader;
	}

	public void setDocumentReader(String documentReader) {
		this.documentReader = documentReader;
	}

	

}
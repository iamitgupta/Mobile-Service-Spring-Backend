package com.ms.entity;

public class Display {
	private String type;
	private String touch;
	private String size;
	private String aspectRatio;
	private String ppi;
	private String screenToBodyRatio;
	private String glassType;
	private String features;
	private String notch;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTouch() {
		return touch;
	}
	public void setTouch(String touch) {
		this.touch = touch;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getAspectRatio() {
		return aspectRatio;
	}
	public void setAspectRatio(String aspectRatio) {
		this.aspectRatio = aspectRatio;
	}
	public String getPpi() {
		return ppi;
	}
	public void setPpi(String ppi) {
		this.ppi = ppi;
	}
	public String getScreenToBodyRatio() {
		return screenToBodyRatio;
	}
	public void setScreenToBodyRatio(String screenToBodyRatio) {
		this.screenToBodyRatio = screenToBodyRatio;
	}
	public String getGlassType() {
		return glassType;
	}
	public void setGlassType(String glassType) {
		this.glassType = glassType;
	}
	public String getFeatures() {
		return features;
	}
	public void setFeatures(String features) {
		this.features = features;
	}
	public String getNotch() {
		return notch;
	}
	public void setNotch(String notch) {
		this.notch = notch;
	}

	

}
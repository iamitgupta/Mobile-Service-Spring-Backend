package com.ms.entity;

public class Technical {
	private String os;
	private String chipset;
	private String cpu;
	private String coreDetails;
	private String gpu;
	private String ipRating;
	private String java;
	private String browser;

	public String getOs() {
		return os;
	}

	public void setOs(String os) {
		this.os = os;
	}

	public String getChipset() {
		return chipset;
	}

	public void setChipset(String chipset) {
		this.chipset = chipset;
	}

	public String getCpu() {
		return cpu;
	}

	public void setCpu(String cpu) {
		this.cpu = cpu;
	}

	public String getCoreDetails() {
		return coreDetails;
	}

	public void setCoreDetails(String coreDetails) {
		this.coreDetails = coreDetails;
	}

	public String getGpu() {
		return gpu;
	}

	public void setGpu(String gpu) {
		this.gpu = gpu;
	}

	public String getIpRating() {
		return ipRating;
	}

	public void setIpRating(String ipRating) {
		this.ipRating = ipRating;
	}

	public String getJava() {
		return java;
	}

	public void setJava(String java) {
		this.java = java;
	}

	public String getBrowser() {
		return browser;
	}

	public void setBrowser(String browser) {
		this.browser = browser;
	}

}